package com.easymco.models;

public class RewardPointsData {

    private String mDateAdded, mDescription, mPoints;

    public String getmDateAdded() {
        return mDateAdded;
    }

    public String getmDescription() {
        return mDescription;
    }

    public String getmPoints() {
        return mPoints;
    }

    public void setmDateAdded(String mDateAdded) {
        this.mDateAdded = mDateAdded;
    }

    public void setmDescription(String mDescription) {
        this.mDescription = mDescription;
    }

    public void setmPoints(String mPoints) {
        this.mPoints = mPoints;
    }
}
